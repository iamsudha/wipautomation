package com.wip.automation.exercise5;


public class Calculator {
  // global variables and methods functionality that can be called from main


  int sumofIntegers(int num1, int num2) {
    int sum = num1 + num2;
    return sum;
  }

  int prodofIntegers(int num1, int num2) {
    int prod = num1 * num2;
    return prod;
  }


  int subtractionofIntegers(int num1, int num2) {
    int result = num1 - num2;
    return result;
  }

  int divisionofIntegers(int num1, int num2) {
    int result = num1 / num2;
    return result;
  }

  int modofIntegers(int num1, int num2) {
    int result = num1 % num2;
    return result;
  }

  int squareofNumber(int num1) {
    int result = num1 * num1;
    return result;
  }

}


